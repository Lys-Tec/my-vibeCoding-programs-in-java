
public class EvenOrOdd2{
    public static void main(String [] args){

        for(int i = 1; i <= 10000; i++){
                if ( i % 2 == 0){
                        System.out.println(i + " is Even");
                } else {
                   System.out.println("Hence " + i + " is Odd");
         }
      }
   }
}